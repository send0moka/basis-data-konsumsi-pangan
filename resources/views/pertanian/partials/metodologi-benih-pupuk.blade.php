            <h4 class="text-lg font-semibold text-neutral-700 mb-2">Definisi dan Konsep</h4>
            <dl class="text-neutral-600 space-y-2">
                <div>
                    <dt class="font-semibold">Benih:</dt>
                    <dd>Bagian tanaman yang digunakan untuk perbanyakan dan/atau pengembangbiakan tanaman.</dd>
                </div>
                <div>
                    <dt class="font-semibold">Pupuk:</dt>
                    <dd>Bahan yang ditambahkan ke tanah atau tanaman untuk menyediakan nutrisi yang dibutuhkan untuk pertumbuhan tanaman.</dd>
                </div>
                <div>
                    <dt class="font-semibold">Alokasi:</dt>
                    <dd>Jumlah benih/pupuk yang dialokasikan untuk distribusi.</dd>
                </div>
                <div>
                    <dt class="font-semibold">Realisasi:</dt>
                    <dd>Jumlah benih/pupuk yang benar-benar didistribusikan.</dd>
                </div>
            </dl>

