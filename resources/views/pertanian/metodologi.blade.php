    <div class="grid md:grid-cols-2 gap-8">
        <!-- Sumber Data -->
        <div>
            <h4 class="text-lg font-medium text-neutral-900 mb-4">Sumber Data</h4>
            <ul class="space-y-2 text-neutral-700">
                <li>• Kementerian Pertanian Republik Indonesia</li>
                <li>• Direktorat Jenderal Tanaman Pangan</li>
