                <li>• Badan Pusat Statistik (BPS)</li>
                <li>• Dinas Pertanian Provinsi dan Kabupaten/Kota</li>
            </ul>
        </div>
        
        <!-- Definisi dan Konsep -->
        <div>
            <h4 class="text-lg font-medium text-neutral-900 mb-4">Definisi dan Konsep</h4>
            <div class="space-y-3 text-neutral-700">
                <div>
                    <strong>Benih:</strong> Bagian tanaman yang digunakan untuk perbanyakan dan/atau pengembangbiakan tanaman.
                </div>
                <div>
                    <strong>Pupuk:</strong> Bahan yang ditambahkan ke tanah atau tanaman untuk menyediakan nutrisi yang dibutuhkan untuk pertumbuhan tanaman.
                </div>
                <div>
                    <strong>Alokasi:</strong> Jumlah benih/pupuk yang dialokasikan untuk distribusi.
                </div>
                <div>
                    <strong>Realisasi:</strong> Jumlah benih/pupuk yang benar-benar didistribusikan.
                </div>
            </div>
        </div>
        
        <!-- Klasifikasi -->
        <div>
            <h4 class="text-lg font-medium text-neutral-900 mb-4">Klasifikasi</h4>
            
            <h5 class="font-medium text-neutral-800 mb-2">Jenis Benih:</h5>
            <ul class="space-y-1 text-neutral-700 mb-4">
                <li>• <strong>Inbrida:</strong> Benih yang dihasilkan dari persilangan dalam keluarga</li>
                <li>• <strong>Hibrida:</strong> Benih yang dihasilkan dari persilangan dua varietas berbeda</li>
                <li>• <strong>Komposit:</strong> Benih yang dihasilkan dari campuran beberapa varietas</li>
            </ul>
            
            <h5 class="font-medium text-neutral-800 mb-2">Jenis Pupuk:</h5>
            <ul class="space-y-1 text-neutral-700">
                <li>• <strong>Urea:</strong> Pupuk nitrogen dengan kandungan N tinggi (46%)</li>
                <li>• <strong>SP-36:</strong> Pupuk fosfor dengan kandungan P2O5 (36%)</li>
                <li>• <strong>ZA:</strong> Pupuk nitrogen dengan kandungan N (21%) dan S (24%)</li>
                <li>• <strong>NPK:</strong> Pupuk majemuk mengandung Nitrogen, Fosfor, dan Kalium</li>
                <li>• <strong>Organik:</strong> Pupuk yang berasal dari bahan organik</li>
            </ul>
        </div>
        
        <!-- Metodologi Pengumpulan -->
        <div>
            <h4 class="text-lg font-medium text-neutral-900 mb-4">Metodologi Pengumpulan Data</h4>
            <ol class="space-y-2 text-neutral-700">
                <li><strong>1. Survei Lapangan:</strong> Pengumpulan data langsung dari petani, kios, dan distributor</li>
                <li><strong>2. Laporan Administratif:</strong> Data dari catatan resmi pemerintah daerah</li>
                <li><strong>3. Sensus Pertanian:</strong> Data komprehensif setiap 10 tahun</li>
                <li><strong>4. Validasi:</strong> Verifikasi silang data dari berbagai sumber</li>
            </ol>
        </div>
        
        <!-- Satuan Pengukuran -->
        <div>
            <h4 class="text-lg font-medium text-neutral-900 mb-4">Satuan Pengukuran</h4>
            <div class="space-y-2 text-neutral-700">
                <div><strong>Ton:</strong> Satuan utama untuk mengukur volume benih dan pupuk</div>
                <div><strong>Kg/Ha:</strong> Satuan untuk mengukur tingkat penggunaan per hektar</div>
                <div><strong>Persentase:</strong> Untuk mengukur tingkat realisasi terhadap alokasi</div>
            </div>
        </div>
        
        <!-- Periode Data -->
        <div>
            <h4 class="text-lg font-medium text-neutral-900 mb-4">Periode dan Frekuensi Data</h4>
            <div class="space-y-2 text-neutral-700">
                <div><strong>Periode:</strong> Data tersedia dari tahun 2014 hingga sekarang</div>
                <div><strong>Frekuensi:</strong> Data dikumpulkan bulanan dan dikonsolidasikan tahunan</div>
                <div><strong>Update:</strong> Data diperbarui setiap bulan setelah validasi</div>
            </div>
        </div>
